<?php
    //  小数
    $var = 0.25;
    var_dump($var);
    echo "<br>";

    // 文字列
    $var = "PHP";
    var_dump($var);
    echo "<br>";

    // 論理値
    $var = true;
    var_dump($var);
    echo "<br>";

    // null
    $var = null;
    var_dump($var);
    echo "<br>";

    // unset関数で$varを消去
    unset($var);

